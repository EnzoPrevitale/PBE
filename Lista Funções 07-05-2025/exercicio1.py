# Promoção relâmpago
def dobro(preco):
    return 2 * preco

preco_produto_A = 5
preco_produto_B = 8
preco_produto_C = 12

print(f"Preço A: {preco_produto_A}, 2 x Preço A: {dobro(preco_produto_A)}")
print(f"Preço B: {preco_produto_B}, 2 x Preço B: {dobro(preco_produto_B)}")
print(f"Preço C: {preco_produto_C}, 2 x Preço C: {dobro(preco_produto_C)}")